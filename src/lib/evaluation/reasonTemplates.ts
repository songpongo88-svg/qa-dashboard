export function getReasonTemplate(
  topicCode: string,
  deductionLevel: DeductionLevel
): string {
  if (deductionLevel === "None") {
    return "การดำเนินการในหัวข้อนี้เป็นไปตามมาตรฐานที่กำหนด โดยไม่พบประเด็นที่ส่งผลต่อคุณภาพการประเมินในสาระสำคัญ";
  }

  const topicTemplates = APR_2026_REASON_TEMPLATES[topicCode];
  if (!topicTemplates) {
    return "";
  }

  return topicTemplates[deductionLevel] || "";
}
